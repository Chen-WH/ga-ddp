function Y = CGA_2clifford(X)
% 将数组形式的CGA元素转换为matlab clifford形式
Y = X(1) + X(2)*ei + X(3)*eo + X(4)*e1 + X(5)*e2 + X(6)*e3 +...
    X(7)*E + X(8)*ei*e1 + X(9)*ei*e2 + X(10)*ei*e3 + X(11)*eo*e1 + X(12)*eo*e2 + X(13)*eo*e3 + X(14)*e23 + X(15)*e3*e1 + X(16)*e12 +...
    X(17)*E*e1 + X(18)*E*e2 + X(19)*E*e3 + X(20)*ei*e23 + X(21)*ei*e3*e1 + X(22)*ei*e12 + X(23)*eo*e23 + X(24)*eo*e3*e1 + X(25)*eo*e12 + X(26)*e123 +...
    X(27)*E*e23 + X(28)*E*e3*e1 + X(29)*E*e12 + X(30)*ei*e123 + X(31)*eo*e123 + X(32)*E*e123;
end