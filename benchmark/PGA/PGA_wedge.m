function res = PGA_wedge(l, r, varargin)
% basis vector = ["1", "e0", "e1", "e2", "e3", "e01", "e02", "e03", "e23", "e31", "e12", "e123", "e023", "e031", "e012", "e0123"]
res = [l(1)*r(1); % 1
    l(1)*r(2) + l(2)*r(1); % e0
    l(1)*r(3) + l(3)*r(1); % e1
    l(1)*r(4) + l(4)*r(1); % e2
    l(1)*r(5) + l(5)*r(1); % e3
    l(2)*r(3) - l(3)*r(2) + l(1)*r(6) + l(6)*r(1); % e01
    l(2)*r(4) - l(4)*r(2) + l(1)*r(7) + l(7)*r(1); % e02
    l(2)*r(5) - l(5)*r(2) + l(1)*r(8) + l(8)*r(1); % e03
    l(4)*r(5) - l(5)*r(4) + l(1)*r(9) + l(9)*r(1); % e23
    l(5)*r(3) - l(3)*r(5) + l(1)*r(10) + l(10)*r(1); % e31
    l(3)*r(4) - l(4)*r(3) + l(1)*r(11) + l(11)*r(1); % e12
    l(3)*r(9) + l(9)*r(3) + l(1)*r(12) + l(12)*r(1) + l(4)*r(10) + l(10)*r(4) + l(5)*r(11) + l(11)*r(5); % e123
    l(2)*r(9) + l(9)*r(2) - l(4)*r(8) + l(5)*r(7) + l(7)*r(5) - l(8)*r(4) + l(1)*r(13) + l(13)*r(1); % e023
    l(3)*r(8) - l(5)*r(6) - l(6)*r(5) + l(8)*r(3) + l(2)*r(10) + l(10)*r(2) + l(1)*r(14) + l(14)*r(1); % e031
    l(4)*r(6) - l(3)*r(7) + l(6)*r(4) - l(7)*r(3) + l(2)*r(11) + l(11)*r(2) + l(1)*r(15) + l(15)*r(1); % e012
    l(2)*r(12) - l(12)*r(2) + l(6)*r(9) + l(9)*r(6) - l(3)*r(13) + l(13)*r(3) + l(1)*r(16) + l(7)*r(10) + l(10)*r(7) + l(16)*r(1) - l(4)*r(14) + l(14)*r(4) + l(8)*r(11) + l(11)*r(8) - l(5)*r(15) + l(15)*r(5)]; % e0123

if ~isempty(varargin)
    res = PGA_wedge(res, varargin{1}, varargin{2:end});
end

end