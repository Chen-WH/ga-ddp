      B(2)*L(4) - B(1)*L(5) - B(4)*L(2) + B(5)*L(1);
      B(6)*L(5) - B(5)*L(6);
      B(4)*L(6) - B(6)*L(4);
      B(5)*L(4) - B(4)*L(5)];
end