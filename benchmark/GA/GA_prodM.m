function M = GA_prodM(M1, M2, varargin)
% 对两个 motor 做几何积运算 ["1", "e01", "e02", "e03", "e23", "e31", "e12", "e0123"]
% 输出的量是新的 Motor
% M = M1*M2 (48M 40A)
M = [M1(1)*M2(1) - M1(5)*M2(5) - M1(6)*M2(6) - M1(7)*M2(7);
     M1(1)*M2(2) + M1(2)*M2(1) - M1(3)*M2(7) + M1(4)*M2(6) - M1(6)*M2(4) + M1(7)*M2(3) - M1(5)*M2(8) - M1(8)*M2(5);
     M1(1)*M2(3) + M1(3)*M2(1) + M1(2)*M2(7) - M1(4)*M2(5) + M1(5)*M2(4) - M1(7)*M2(2) - M1(6)*M2(8) - M1(8)*M2(6);
     M1(1)*M2(4) + M1(4)*M2(1) - M1(2)*M2(6) + M1(3)*M2(5) - M1(5)*M2(3) + M1(6)*M2(2) - M1(7)*M2(8) - M1(8)*M2(7);
     M1(1)*M2(5) + M1(5)*M2(1) - M1(6)*M2(7) + M1(7)*M2(6);
     M1(1)*M2(6) + M1(6)*M2(1) + M1(5)*M2(7) - M1(7)*M2(5);
     M1(1)*M2(7) + M1(7)*M2(1) - M1(5)*M2(6) + M1(6)*M2(5);
     M1(2)*M2(5) + M1(5)*M2(2) + M1(1)*M2(8) + M1(3)*M2(6) + M1(6)*M2(3) + M1(8)*M2(1) + M1(4)*M2(7) + M1(7)*M2(4)];
if ~isempty(varargin)
    M = GA_prodM(M, varargin{1}, varargin{2:end});
end
end